package com.rahul.kumar.scaler.Module3Day16.CarryForwardAndSubArrayOfString;

public class Program2PrintTheSubArrayOfTheArray {

	public static void main(String[] args) {
		int [] arr = {2,4,5,6,8,10,75,88};
		int startindex =2;
		int endeIndex = 6;
		for(int i=startindex;i<endeIndex;i++) {
			System.out.print(arr[i]+" ");
		}
	}
}
